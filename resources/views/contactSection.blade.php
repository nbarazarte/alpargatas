						<h4>Who created Bitcoin?</h4>
						<i class="vc_toggle_icon"></i>
					</div>
					<div class="vc_toggle_content">
						<p>
							Bitcoin is the first implementation of a concept called &#8220;cryptocurrency&#8221;, which was first described in 1998 by Wei Dai on the cypherpunks mailing list, suggesting the idea of a new form of money that uses cryptography to control its creation and transactions, rather than a central authority. The first Bitcoin specification and proof of concept was published in 2009 in a cryptography mailing list by Satoshi Nakamoto. Satoshi left the project in late 2010 without revealing much about himself. The community has since grown exponentially with many developers working on Bitcoin.
						</p>
					</div>
				</div>
				<div class="vc_toggle vc_toggle_arrow vc_toggle_color_default  vc_toggle_size_md">
