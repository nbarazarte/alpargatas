						<h4>Who controls the Bitcoin network?</h4>
						<i class="vc_toggle_icon"></i>
					</div>
					<div class="vc_toggle_content">
						<p>
							Nobody owns the Bitcoin network much like no one owns the technology behind email. 	Bitcoin is controlled by all Bitcoin users around the world. While developers are improving the software, they can&#8217;t force a change in the Bitcoin protocol because all users are free to choose what software and version they use. In order to stay compatible with each other, all users need to use software complying with the same rules. Bitcoin can only work correctly with a complete consensus among all users. Therefore, all users and developers have a strong incentive to protect this consensus.
						</p>
					</div>
				</div>
				<div class="vc_toggle vc_toggle_arrow vc_toggle_color_default  vc_toggle_size_md">
					<div class="vc_toggle_title">
						<h4>How does Bitcoin work?</h4>
						<i class="vc_toggle_icon"></i>
					</div>
					<div class="vc_toggle_content">
						<p>
							From a user perspective, Bitcoin is nothing more than a mobile app or computer program that provides a personal Bitcoin wallet and allows a user to send and receive bitcoins with them. This is how Bitcoin works for most users.
						</p>
						<p>
							Behind the scenes, the Bitcoin network is sharing a public ledger called the &#8220;block chain&#8221;. This ledger contains every transaction ever processed, allowing a user&#8217;s computer to verify the validity of each transaction. The authenticity of each transaction is protected by digital signatures corresponding to the sending addresses, allowing all users to have full control over sending bitcoins from their own Bitcoin addresses. In addition, anyone can process transactions using the computing power of specialized hardware and earn a reward in bitcoins for this service. This is often called &#8220;mining&#8221;. To learn more about Bitcoin, you can consult the dedicated page and the original paper.
						</p>
					</div>
				</div>

